#include <unistd.h>

int	main(int ac, char **av)
{
	int	tab[256] = {0};
	int	i;
	int	j;

	if (ac == 3)
	{
		for (i = 1; i < 3; i++)
		{
			j = 0;
			while (av[i][j])
			{
				if (tab[(int)av[i][j]] == 0)
				{
					write (1, &av[i][j], 1);
					tab[(int)av[i][j]] = 1;
				}
				j++;
			}
		}
	}
	write(1, "\n", 1);
	return (0);
}
